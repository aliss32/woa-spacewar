# ── INF Patcher ───────────────────────────────────────────────────────────────

def patch_inf(filepath, driver_type):
    entry = SPACEWAR_DEVICE_IDS.get(driver_type)
    if not entry:
        return False
    if not entry["confirmed"]:
        print(f"  ⏳ {driver_type}: skipped (TODO — needs hardware analysis)")
        return False

    content = read_file(filepath)
    changed = False

    for hw_id in entry["ids"]:
        if hw_id in content or hw_id == "TODO_TOUCH_DEVICE_ID" or hw_id == "TODO_FP_DEVICE_ID":
            continue

        # Insert before [Strings] section
        inject = (
            f"\n; Spacewar (Nothing Phone 1) — {entry['description']}\n"
            f"; Source: {entry['source']}\n"
            f"%DeviceDesc% = Install, {hw_id}\n"
        )
        content = re.sub(r'(\[Strings\])', inject + r'\1', content, count=1)
        changed = True

    if changed:
        with open(filepath, 'w', encoding='utf-8') as f:
            f.write(content)
        print(f"  ✅ {driver_type}: patched {os.path.basename(filepath)}")

    return changed


def read_file(path):
    try:
        with open(path, 'r', encoding='utf-8', errors='ignore') as f:
            return f.read()
    except Exception:
        return ""


