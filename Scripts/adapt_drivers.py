# ── Main ──────────────────────────────────────────────────────────────────────

def adapt_drivers(source_path, output_path):
    print("\n🔧 WOA-Spacewar Driver Adaptation")
    print("=" * 45)
    print(f"   Source : {source_path}")
    print(f"   Output : {output_path}\n")

    # Copy Lisa drivers to output
    if os.path.exists(output_path):
        shutil.rmtree(output_path)
    shutil.copytree(source_path, output_path)
    print(f"✅ Copied Lisa driver pack to {output_path}\n")

    updated = skipped = todo = 0

    for root, _, files in os.walk(output_path):
        for filename in files:
            if not filename.lower().endswith('.inf'):
                continue

            filepath = os.path.join(root, filename)
            name_lower = filename.lower()

            driver_type = None
            for kw, dtype in INF_KEYWORDS.items():
                if kw in name_lower:
                    driver_type = dtype
                    break

            if not driver_type:
                continue

            entry = SPACEWAR_DEVICE_IDS.get(driver_type, {})
            if not entry.get("confirmed", False):
                todo += 1
                continue

            result = patch_inf(filepath, driver_type)
            if result:
                updated += 1
            else:
                skipped += 1

    print(f"\n{'='*45}")
    print(f"  ✅ Patched : {updated} files")
    print(f"  ⏭️  Skipped : {skipped} files (already patched)")
    print(f"  ⏳ TODO    : {todo} files (need hardware analysis)")
    print(f"{'='*45}")
    print("\nNext step: Run analyze.yml → then re-run this script")
    print("           to patch Touch and Fingerprint drivers.\n")


def main():
    parser = argparse.ArgumentParser()
    parser.add_argument("source", help="Lisa driver pack path")
    parser.add_argument("output", help="Output path for Spacewar drivers")
    args = parser.parse_args()
    adapt_drivers(args.source, args.output)


if __name__ == "__main__":
    main()
